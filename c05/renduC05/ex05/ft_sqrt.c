int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (nb > 0)
	{
		nb = nb - ((2 * i) + 1);
		i++;
	}
	if (nb < 0)
		return (0);
	return (i);
}
#include <stdio.h>
int	main(void)
{
	printf("%d", ft_sqrt(16));
}
